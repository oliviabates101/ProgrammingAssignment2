## these functions cache the inverse of matricies and retreive it, 
##but only work in matricies that are invertible

## this function that creates a special matrix, a list that can cache 
#its inverse

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) i <<- solve
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## this function computes the inverse of the special matrix, returned from makeCacheMatrix
#if the solution has already been calculated, then cachesolve retrives the solution from the cache

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
